import { Metadata } from 'next';
import { notFound } from 'next/navigation';
import { ErgoPrivacyClient } from './ErgoPrivacyClient';
import { infographics } from '@/data/infographics';
import { siteConfig } from '@/config/site-config';

interface Props {
  params: { locale: string };
}

const SLUG = 'ergo-privacy-non-interactive-efficient';

export async function generateMetadata({ params }: Props): Promise<Metadata> {
  const infographic = infographics.find((item) => item.slug === SLUG);

  if (!infographic) {
    return {
      title: 'Infographic Not Found',
      description: 'The requested infographic could not be found.',
    };
  }

  const origin = siteConfig.siteUrl;
  const url = `${origin}/infographics/${infographic.slug}`;

  const title =
    infographic.seoTitle ||
    'Ergo Privacy: Sigma Protocols, eUTXO, ErgoMixer & NiPoPoWs';
  const description =
    infographic.seoDescription ||
    'Discover how Ergo delivers non-interactive, efficient privacy using Sigma Protocols, the eUTXO model, ErgoMixer and NiPoPoWs light-client proofs—without trusted third parties.';

  return {
    title,
    description,
    keywords: infographic.tags,
    alternates: { canonical: url },
    openGraph: {
      type: 'article',
      url,
      siteName: 'Ergo Platform',
      title: 'Ergo Privacy: Non-Interactive & Efficient',
      description:
        'Infographic overview of Ergo’s privacy stack: zero-knowledge Sigma Protocols, the eUTXO model, decentralized ErgoMixer and NiPoPoWs, all providing built-in privacy without custodians.',
      images: [
        {
          url: `${origin}${infographic.fullImageUrl}`,
          width: 1280,
          height: 670,
          alt: infographic.imageAlt,
        },
      ],
      locale: 'en_US',
    },
    twitter: {
      card: 'summary_large_image',
      title,
      description,
      images: [`${origin}${infographic.fullImageUrl}`],
      site: siteConfig.twitterHandle,
      creator: siteConfig.twitterHandle,
    },
    robots: { index: true, follow: true },
  };
}

export default function ErgoPrivacyPage({ params }: Props) {
  const infographic = infographics.find((item) => item.slug === SLUG);

  if (!infographic) {
    notFound();
  }

  const structuredData = {
    '@context': 'https://schema.org',
    '@type': 'ImageObject',
    name: infographic.title,
    description: infographic.shortDescription,
    url: `${siteConfig.siteUrl}${infographic.fullImageUrl}`,
    contentUrl: `${siteConfig.siteUrl}${infographic.fullImageUrl}`,
    thumbnailUrl: `${siteConfig.siteUrl}${infographic.previewImageUrl}`,
    datePublished: infographic.publishDate,
    author: {
      '@type': 'Organization',
      name: 'Ergo Platform',
    },
    publisher: {
      '@type': 'Organization',
      name: 'Ergo Platform',
      logo: {
        '@type': 'ImageObject',
        url: `${siteConfig.siteUrl}/logo.png`,
      },
    },
    keywords: infographic.tags.join(', '),
    about: [
      {
        '@type': 'Thing',
        name: 'Ergo privacy',
      },
      {
        '@type': 'Thing',
        name: 'Sigma Protocols',
      },
      {
        '@type': 'Thing',
        name: 'ErgoMixer',
      },
      {
        '@type': 'Thing',
        name: 'NiPoPoWs light clients',
      },
    ],
  };

  return (
    <>
      <script
        type="application/ld+json"
        // eslint-disable-next-line react/no-danger
        dangerouslySetInnerHTML={{
          __html: JSON.stringify(structuredData),
        }}
      />
      <ErgoPrivacyClient infographic={infographic} />
    </>
  );
}


