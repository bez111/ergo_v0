// Helper functions for filtering and sorting
export function filterInfographics(
  infographics: InfographicMeta[],
  filters: {
    category?: string;
    level?: string;
    search?: string;
    sort?: string;
  }
): InfographicMeta[] {
  let filtered = [...infographics];

  // Filter by category
  if (filters.category && filters.category !== 'all') {
    filtered = filtered.filter(item => item.category === filters.category);
  }

  // Filter by level
  if (filters.level && filters.level !== 'all') {
    filtered = filtered.filter(item => item.level === filters.level);
  }

  // Filter by search
  if (filters.search) {
    const searchLower = filters.search.toLowerCase();
    filtered = filtered.filter(item => 
      item.title.toLowerCase().includes(searchLower) ||
      item.shortDescription.toLowerCase().includes(searchLower) ||
      item.tags.some(tag => tag.toLowerCase().includes(searchLower))
    );
  }

  // Sort
  switch (filters.sort) {
    case 'alphabetical': {
      filtered.sort((a, b) => a.title.localeCompare(b.title));
      break;
    }
    case 'popular': {
      // Curated popularity: comparisons & narratives first, then core tech
      const categoryPriority: Record<string, number> = {
        // 1) High-level Ergo positioning & intro
        'ergo-overview': 4,
        // 2) Narratives vs VC / banking etc.
        'vc-chains-narratives': 3,
        // 3) Comparisons with other L1s, CBDCs, etc.
        'comparisons-matrices': 2,
        // 4) Deeper technical infographics
        'consensus-mining': 1,
        'eutxo-smart-contracts': 1,
        'storage-rent-economics': 1,
        'privacy-sigma': 1,
        'nipopows-light-clients': 1,
        'dev-tooling': 1,
        'scalability-performance': 1,
        'scalability-settlement': 1,
      };

      const levelPriority: Record<string, number> = {
        beginner: 3,
        intermediate: 2,
        advanced: 1,
      };

      filtered.sort((a, b) => {
        const catA = categoryPriority[a.category] ?? 0;
        const catB = categoryPriority[b.category] ?? 0;
        if (catA !== catB) return catB - catA; // higher priority first

        const lvlA = levelPriority[a.level] ?? 0;
        const lvlB = levelPriority[b.level] ?? 0;
        if (lvlA !== lvlB) return lvlB - lvlA; // beginner before advanced

        // Newer first inside same bucket
        return new Date(b.publishDate).getTime() - new Date(a.publishDate).getTime();
      });
      break;
    }
    case 'newest':
    default: {
      filtered.sort((a, b) => new Date(b.publishDate).getTime() - new Date(a.publishDate).getTime());
      break;
    }
  }

  return filtered;
}

export function getInfographicBySlug(slug: string): InfographicMeta | undefined {
  return infographics.find(item => item.slug === slug);
}

export function getRelatedInfographics(current: InfographicMeta, limit: number = 3): InfographicMeta[] {
  return infographics
    .filter(item => item.slug !== current.slug && item.category === current.category)
    .slice(0, limit);
}
